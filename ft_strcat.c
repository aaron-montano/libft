#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*dest;
	char	*src;

	dest = s1;
	src = (char *)s2;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (dest);
}
